// Generate a puzzle from validated pangram candidates
function generatePuzzleFromPangrams() {
    if (validatedPuzzles.length === 0) {
        return null;
    }
    
    // Select a random validated puzzle
    const puzzle = validatedPuzzles[Math.floor(Math.random() * validatedPuzzles.length)];
    
    return {
        pangram: puzzle.pangram,
        letters: puzzle.letters,
        centerLetter: puzzle.centerLetter,
        wordCount: 50 // Estimated, will be validated during play
    };
}

